class DoublePrecisionConstants {
    public static void main(String[] args) {
        double d1 = 1.2;
        double d2 = 0.0001;
        double d3 = 1e100;
        double d4 = 1e-100;
        double d5 = 1.5e-100;

        System.out.println("d1=" + d1);
        System.out.println("d2=" + d2);
        System.out.println("d3=" + d3);
        System.out.println("d4=" + d4);
        System.out.println("d5=" + d5);
    }
}

