class LogicalOperators {
    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;

        boolean result1 = !x;
        boolean result2 = !y;
        boolean result3 = x && y;
        boolean result4 = x || y;

        System.out.println("result1 = " + result1);
        System.out.println("result2 = " + result2);
        System.out.println("result3 = " + result3);
        System.out.println("result4 = " + result4);
    }
}

