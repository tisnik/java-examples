class IntegerTypes {
    public static void main(String[] args) {
        byte  b = 100;
        short s = 10000;
        int   i = 100000;
        long  l = 1000000;
        System.out.println("b=" + b);
        System.out.println("s=" + s);
        System.out.println("i=" + i);
        System.out.println("l=" + l);
    }
}

