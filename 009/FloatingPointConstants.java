class FloatingPointConstants {
    public static void main(String[] args) {
        float f1 = 1.2f;
        float f2 = 0.0001f;
        float f3 = 1e10f;
        float f4 = 1e-10f;
        float f5 = 1.5e-10f;

        System.out.println("f1=" + f1);
        System.out.println("f2=" + f2);
        System.out.println("f3=" + f3);
        System.out.println("f4=" + f4);
        System.out.println("f5=" + f5);
    }
}

